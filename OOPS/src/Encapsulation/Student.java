package Encapsulation;

public class Student {
	String name;
	int id;
	String depmt;
	public String getName()
	{
		return name;
	}
	public void setName(String name) {
		this.name=name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDepmt() {
		return depmt;
	}
	public void setDepmt(String depmt) {
		this.depmt = depmt;
	}
	

		
	
}
